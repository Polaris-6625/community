package example;

public class BigShuRes {
    private String traffic;
    private String serve;
    private String play;
    private String safe;
    private String acti;
    private String beaf;
    private String maintain;
    private String elseL;

    public String getTraffic() {
        return traffic;
    }

    public void setTraffic(String traffic) {
        this.traffic = traffic;
    }

    public String getServe() {
        return serve;
    }

    public void setServe(String serve) {
        this.serve = serve;
    }

    public String getPlay() {
        return play;
    }

    public void setPlay(String play) {
        this.play = play;
    }

    public String getSafe() {
        return safe;
    }

    public void setSafe(String safe) {
        this.safe = safe;
    }

    public String getActi() {
        return acti;
    }

    public void setActi(String acti) {
        this.acti = acti;
    }

    public String getBeaf() {
        return beaf;
    }

    public void setBeaf(String beaf) {
        this.beaf = beaf;
    }

    public String getMaintain() {
        return maintain;
    }

    public void setMaintain(String maintain) {
        this.maintain = maintain;
    }

    public String getElseL() {
        return elseL;
    }

    public void setElseL(String elseL) {
        this.elseL = elseL;
    }

    @Override
    public String toString() {
        return "BigShuRes{" +
                "traffic='" + traffic + '\'' +
                ", serve='" + serve + '\'' +
                ", play='" + play + '\'' +
                ", safe='" + safe + '\'' +
                ", acti='" + acti + '\'' +
                ", beaf='" + beaf + '\'' +
                ", maintain='" + maintain + '\'' +
                ", elseL='" + elseL + '\'' +
                '}';
    }
}
